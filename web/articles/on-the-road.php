                Aller guten Dinge sind drei (German)
            </div>
            <div class="description">
                <p>
                    Was man hätte lernen können, wenn man drei Mal Qualitätsmanagement in unterschiedlichen Unternehmen aufgebaut hat. Nils und Torsten haben das Qualitätsmanagement im Digitalbereich
                    bei Europas größten Verlagshaus etabliert. Dann trennten sich ihre Wege, damit jeder für sich in einer anderen Firma sich noch mal dem Thema Qualität widmen kann.
                </p>
                <p>
                    Zusammen blicken
                    die beiden auf ihre gemeinsame “dunkle” Vergangenheit zurück, erzählen was alles schiefgehen kann und präsentieren ihre neuen Vorgehensweisen in ihren jetzigen Unternehmen. Danach
                    analysieren sie warum das absolute Chaos ausbrechen würde, wenn man die Methodik des jeweiligen anderen auf das eigene Unternehmen anwendet.
                </p>
                <p>
                    Der Talk befasst sich auf humorvolle Art mit Themen wie Testautomatisierung, Werkzeuge, Business Value und schlanken Vorgehensweisen (Lean Testing).
                </p>

